const TRANSLATIONS = {
  fr: { COACHING: COACHING_FR, NAV: NAV_FR, SYSADMIN: SYSADMIN_FR },
  es: { COACHING: COACHING_ES, NAV: NAV_ES, SYSADMIN: SYSADMIN_ES },
  sk: { COACHING: COACHING_SK, NAV: NAV_SK, SYSADMIN: SYSADMIN_SK },
};

for (const lang of ['fr', 'es', 'sk']) {
  const file = path.join(I18N_DIR, `${lang}.json`);
  const data = JSON.parse(fs.readFileSync(file, 'utf8'));
  let updated = 0;

  for (const ns of Object.keys(TRANSLATIONS[lang])) {
    if (!data[ns]) data[ns] = {};
    for (const [key, value] of Object.entries(TRANSLATIONS[lang][ns])) {
      if (data[ns][key] !== value) {
        data[ns][key] = value;
        updated++;
      }
    }
  }

  fs.writeFileSync(file, JSON.stringify(data, null, 2) + '\n');
  console.log(`  ${lang}.json: ${updated} keys translated`);
}
