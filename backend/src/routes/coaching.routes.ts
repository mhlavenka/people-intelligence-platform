/** Update session (notes, status, ratings). */
router.put(
  '/sessions/:id',
  requirePermission('MANAGE_COACHING'),
  async (req: AuthRequest, res: Response, next: NextFunction) => {
    try {
      const existing = await CoachingSession.findOne({
        _id: req.params['id'],
        organizationId: req.user!.organizationId,
      });
      if (!existing) { res.status(404).json({ error: req.t('errors.sessionNotFound') }); return; }

      // Once a session is `completed`, the journal is locked. Block edits to
      // notes/templates/ratings, and block status changes (Mark Complete is a
      // one-way transition — re-pressing or re-cancelling is rejected).
      if (existing.status === 'completed') {
        const incomingStatus = req.body.status;
        if (incomingStatus !== undefined && incomingStatus !== 'completed') {
          res.status(409).json({ error: req.t('errors.sessionLockedCompleted') });
          return;
        }
        for (const field of SESSION_LOCKED_FIELDS) {
          if (!(field in req.body)) continue;
          const next = req.body[field];
          const cur = (existing as any)[field];
          const changed = Array.isArray(cur)
            ? JSON.stringify([...cur].sort()) !== JSON.stringify([...(next ?? [])].sort())
            : String(cur ?? '') !== String(next ?? '');
          if (changed) {
            res.status(409).json({ error: req.t('errors.sessionLockedCompleted') });
            return;
          }
        }
      }

      if (
        req.body.preSessionIntakeTemplateId
        && req.body.preSessionIntakeTemplateId !== existing.preSessionIntakeTemplateId?.toString()
      ) {
        await assertValidPreSessionIntake(
          req.body.preSessionIntakeTemplateId,
          req.user!.organizationId,
        );
      }
      if (
        req.body.postSessionIntakeTemplateId
        && req.body.postSessionIntakeTemplateId !== existing.postSessionIntakeTemplateId?.toString()
      ) {
        await assertValidPostSessionIntake(
          req.body.postSessionIntakeTemplateId,
          req.user!.organizationId,
        );
      }

      const preSessionTemplateChanged =
        req.body.preSessionIntakeTemplateId
        && req.body.preSessionIntakeTemplateId !== existing.preSessionIntakeTemplateId?.toString();

      const wasNotCompleted = existing.status !== 'completed';
      const prev = { date: existing.date, status: existing.status };
      Object.assign(existing, req.body);
      await existing.save();

      // Notify coachee when a pre-session assessment form is added/changed
      if (preSessionTemplateChanged) {
        (async () => {
          try {
            const [coachee, coach, template] = await Promise.all([
              User.findById(existing.coacheeId).select('firstName email preferredLanguage'),
              User.findById(existing.coachId).select('firstName lastName'),
              SurveyTemplate.findById(existing.preSessionIntakeTemplateId).select('title'),
            ]);
            if (!coachee?.email || !coach || !template) return;

            const coachName = `${coach.firstName} ${coach.lastName}`;
            const sessionDate = existing.date.toLocaleDateString(coachee.preferredLanguage || 'en', {
              weekday: 'long', year: 'numeric', month: 'long', day: 'numeric',
            });
            const intakeUrl = `${config.frontendUrl}/intake/${template._id}?sessionId=${existing._id}`;

            await sendEmail({
              to: coachee.email,
              subject: `Pre-session form ready — ${sessionDate}`,
              html: `<p>Hi ${coachee.firstName},</p>
                     <p>${coachName} has added a pre-session form for your session on <strong>${sessionDate}</strong>.</p>
                     <p><a href="${intakeUrl}">Complete Pre-Session Form: ${template.title}</a></p>`,
            });

            await notifyPreSessionForm({
              coacheeId: existing.coacheeId,
              organizationId: req.user!.organizationId,
              coachName,
              sessionDate,
              templateTitle: template.title,
              intakeUrl,
            });
          } catch (err) {
            console.error('[Coaching] Failed to notify coachee about pre-session form:', err);
          }
        })();
      }

      // Mirror date / status changes into the linked Booking
      await propagateSessionUpdate(existing, prev).catch((err) =>
        console.error('[BookingSync] Failed to propagate session update:', err),
      );

      // If status changed to completed, increment engagement counter and send
      // the post-session form. Coach-assigned template wins; otherwise we fall
      // back to AI auto-generation when context is available.
      // Chemistry calls are complimentary and skip the quota increment.
      if (wasNotCompleted && existing.status === 'completed') {
        if (!existing.isChemistryCall) {
          await CoachingEngagement.findByIdAndUpdate(existing.engagementId, { $inc: { sessionsUsed: 1 } });
        }

        if (existing.postSessionIntakeTemplateId && !existing.postSessionIntakeSentAt) {
          dispatchAssignedPostSessionForm(existing, req.language)
            .catch((err) => console.error('[PostSession] Assigned dispatch failed:', err));
        } else if (!existing.postSessionIntakeTemplateId) {
          const hasCtx = existing.topics.length > 0
            || (existing.sharedNotes ?? '').trim()
            || (existing.coachNotes ?? '').trim();
          if (hasCtx) {
            generateAndSendPostSessionForm(existing, req.user!.organizationId, req.user!.userId, req.language)
              .catch((err) => console.error('[PostSession] Auto-generate failed:', err));
          }
        }
      }

      // Google Calendar sync
      try {
        if (existing.googleEventId && await isCalendarConnected(existing.coachId.toString())) {
          const coachee = await resolveCoachee(existing.coacheeId.toString());
          await gcal.updateCalendarEvent(existing.coachId.toString(), existing.googleEventId, {
            date: existing.date,
            duration: existing.duration,
            coacheeName: coachee.name,
            coacheeEmail: coachee.email,
            sharedNotes: existing.sharedNotes,
            status: existing.status,
          });
        }
      } catch (calErr) {
        console.warn('[GCal] Failed to update event:', calErr);
      }

      await existing.populate([
        { path: 'coacheeId', select: 'firstName lastName profilePicture' },
        { path: 'coachId', select: 'firstName lastName' },
      ]);
      res.json(existing);
    } catch (e) { next(e); }
  }
);

/** Generate a post-session reflection form from session context, create a
 *  SurveyTemplate, link it to the session, and email the coachee. */
router.post(
  '/sessions/:id/post-session-form',
  requirePermission('MANAGE_COACHING'),
  async (req: AuthRequest, res: Response, next: NextFunction) => {
    try {
      const session = await CoachingSession.findOne({
        _id: req.params['id'],
        organizationId: req.user!.organizationId,
      });
      if (!session) { res.status(404).json({ error: req.t('errors.sessionNotFound') }); return; }

      if (session.postSessionIntakeTemplateId) {
        res.status(409).json({ error: req.t('errors.postSessionFormAlreadyGenerated') });
        return;
      }

      const coachee = await User.findById(session.coacheeId).select('firstName lastName email');
      if (!coachee?.email) {
        res.status(400).json({ error: req.t('errors.coacheeNotFoundOrNoEmail') });
        return;
      }

      const summary = typeof req.body.summary === 'string' ? req.body.summary.trim() : '';
      const hasContext = session.topics.length > 0
        || (session.sharedNotes ?? '').trim()
        || (session.coachNotes ?? '').trim()
        || summary;

      if (!hasContext) {
        res.status(400).json({
          error: req.t('errors.noSessionContext'),
        });
        return;
      }

      const prompt = buildPostSessionReflectionPrompt({
        coacheeName: coachee.firstName,
        topics: session.topics,
        sharedNotes: session.sharedNotes || undefined,
        coachNotes: session.coachNotes || undefined,
        summary: summary || undefined,
        growFocus: session.growFocus,
      }, req.language);

      const aiResponse = await callClaude(prompt, undefined, undefined, req.user!.organizationId);

      let questions: Array<{ id: string; text: string; type: string; category: string }>;
      try {
        let clean = aiResponse.replace(/```(?:json)?\r?\n?/g, '').replace(/```/g, '').trim();
        const arrStart = clean.indexOf('[');
        const arrEnd = clean.lastIndexOf(']');
        if (arrStart !== -1 && arrEnd > arrStart) clean = clean.slice(arrStart, arrEnd + 1);
        questions = JSON.parse(clean);
      } catch {
        res.status(500).json({ error: req.t('errors.aiInvalidQuestionFormat') });
        return;
      }

      const sessionDate = session.date.toLocaleDateString(req.language || 'en', {
        day: 'numeric', month: 'short', year: 'numeric',
      });

      const template = await SurveyTemplate.create({
        organizationId: req.user!.organizationId,
        moduleType: 'coaching',
        intakeType: 'assessment',
        title: `Post-Session Reflection — ${sessionDate}`,
        description: `Reflection questions for your coaching session on ${sessionDate}.`,
        instructions: 'Take a few minutes to reflect on your recent coaching session. There are no right or wrong answers — your honest thoughts help both you and your coach.',
        isActive: true,
        isGlobal: false,
        isAutoGenerated: true,
        generatedForSessionId: session._id,
        createdBy: req.user!.userId,
        questions: questions.map((q) => ({
          id: q.id,
          text: q.text,
          type: 'text',
          category: q.category,
        })),
      });

      session.postSessionIntakeTemplateId = template._id as any;
      session.postSessionIntakeSentAt = new Date();
      await session.save();

      const intakeUrl = `${config.frontendUrl}/intake/${template._id}?sessionId=${session._id}`;
      const coach = await User.findById(session.coachId).select('firstName lastName');
      const coachName = coach ? `${coach.firstName} ${coach.lastName}` : 'your coach';

      await sendEmail({
        to: coachee.email,
        subject: `Post-session reflection — ${sessionDate}`,
        html: buildPostSessionEmailHtml({
          coacheeName: coachee.firstName,
          coachName,
          sessionDate,
          intakeUrl,
        }),
      });

      res.status(201).json({
        templateId: template._id,
        questionCount: questions.length,
        sentTo: coachee.email,
      });
    } catch (e) { next(e); }
  }
);

/** Delete session. */
router.delete(
  '/sessions/:id',
  requirePermission('MANAGE_COACHING'),
  async (req: AuthRequest, res: Response, next: NextFunction) => {
    try {
      const session = await CoachingSession.findOneAndDelete({
        _id: req.params['id'],
        organizationId: req.user!.organizationId,
      });
      if (!session) { res.status(404).json({ error: req.t('errors.sessionNotFound') }); return; }

      // If the session has a paired Booking, cancel through cancelBooking()
      // so GCal delete + cancellation email + cache invalidation all run.
      // For sessions without a paired Booking (manually created, no public
      // flow), fall back to deleting the GCal event directly.
      if (session.bookingId) {
        try {
          await cancelBooking(session.bookingId.toString(), 'coach', 'Session deleted');
        } catch (err) {
          console.error('[BookingSync] cancelBooking on session delete failed:', err);
          // Best-effort fallback: at least attempt the GCal delete.
          if (session.googleEventId && await isCalendarConnected(session.coachId.toString())) {
            await gcal.deleteCalendarEvent(session.coachId.toString(), session.googleEventId)
              .catch((calErr) => console.warn('[GCal] fallback delete failed:', calErr));
          }
        }
      } else {
        try {
          if (session.googleEventId && await isCalendarConnected(session.coachId.toString())) {
            await gcal.deleteCalendarEvent(session.coachId.toString(), session.googleEventId);
          }
        } catch (calErr) {
          console.warn('[GCal] Failed to delete event:', calErr);
        }
      }

      res.json({ message: 'Session deleted' });
    } catch (e) { next(e); }
  }
);

