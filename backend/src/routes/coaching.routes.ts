/** List sessions — scoped per role. Coachees see shared notes only. */
router.get('/sessions', async (req: AuthRequest, res: Response, next: NextFunction) => {
  try {
    const base: Record<string, unknown> = {};
    if (req.query['engagementId']) base['engagementId'] = req.query['engagementId'];
    const filter = scopeCoachingFilter(req, base);

    const selectFields = isCoacheeUser(req)
      ? '-coachNotes'   // NEVER expose private coach notes to coachees
      : undefined;

    const sessions = await CoachingSession.find(filter)
      .select(selectFields as string)
      .populate('coacheeId', 'firstName lastName profilePicture')
      .populate('coachId', 'firstName lastName email profilePicture')
      .populate('preSessionIntakeTemplateId', 'title moduleType intakeType')
      .sort({ date: -1 });

    // Annotate each session with whether the coachee has submitted the
    // pre-session intake, so coach + coachee UIs can show the right status.
    const preIds = sessions.filter((s) => s.preSessionIntakeTemplateId).map((s) => s._id);
    const postIds = sessions.filter((s) => s.postSessionIntakeTemplateId).map((s) => s._id);
    const allIds = [...new Set([...preIds, ...postIds].map(String))];
    const intakeResponses = allIds.length
      ? (await SurveyResponse.find({
          sessionId: { $in: allIds },
          organizationId: req.user!.organizationId,
        }).select('sessionId templateId').lean())
      : [];

    const preCompletedIds = new Set(
      intakeResponses
        .filter((r) => preIds.some((id) => String(id) === String(r.sessionId)))
        .map((r) => r.sessionId?.toString()),
    );
    const postCompletedIds = new Set(
      intakeResponses
        .filter((r) => postIds.some((id) => String(id) === String(r.sessionId))
          && sessions.find((s) => String(s._id) === String(r.sessionId))
              ?.postSessionIntakeTemplateId?.toString() === r.templateId?.toString())
        .map((r) => r.sessionId?.toString()),
    );

    const result = sessions.map((s) => ({
      ...s.toObject(),
      preSessionIntakeCompleted: preCompletedIds.has((s._id as { toString(): string }).toString()),
      postSessionIntakeCompleted: postCompletedIds.has((s._id as { toString(): string }).toString()),
    }));
    res.json(result);
  } catch (e) { next(e); }
});

