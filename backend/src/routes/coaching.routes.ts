async function generateAndSendPostSessionForm(
  session: InstanceType<typeof CoachingSession>,
  organizationId: string,
  coachUserId: string,
  language: string = 'en',
): Promise<void> {
  const coachee = await User.findById(session.coacheeId).select('firstName lastName email');
  if (!coachee?.email) return;

  const prompt = buildPostSessionReflectionPrompt({
    coacheeName: coachee.firstName,
    topics: session.topics,
    sharedNotes: session.sharedNotes || undefined,
    coachNotes: session.coachNotes || undefined,
    growFocus: session.growFocus,
  }, language);

  const aiResponse = await callClaude(prompt, undefined, undefined, organizationId);
  let questions: Array<{ id: string; text: string; type: string; category: string }>;
  try {
    let clean = aiResponse.replace(/```(?:json)?\r?\n?/g, '').replace(/```/g, '').trim();
    const arrStart = clean.indexOf('[');
    const arrEnd = clean.lastIndexOf(']');
    if (arrStart !== -1 && arrEnd > arrStart) clean = clean.slice(arrStart, arrEnd + 1);
    questions = JSON.parse(clean);
  } catch {
    console.warn('[PostSession] AI returned unparseable questions');
    return;
  }

  const sessionDate = session.date.toLocaleDateString(language, {
    day: 'numeric', month: 'short', year: 'numeric',
  });

  const template = await SurveyTemplate.create({
    organizationId,
    moduleType: 'coaching',
    intakeType: 'assessment',
    title: `Post-Session Reflection — ${sessionDate}`,
    description: `Reflection questions for your coaching session on ${sessionDate}.`,
    instructions: 'Take a few minutes to reflect on your recent coaching session. There are no right or wrong answers.',
    isActive: true,
    isGlobal: false,
    isAutoGenerated: true,
    generatedForSessionId: session._id,
    createdBy: coachUserId,
    questions: questions.map((q) => ({
      id: q.id, text: q.text, type: 'text', category: q.category,
    })),
  });

  session.postSessionIntakeTemplateId = template._id as any;
  session.postSessionIntakeSentAt = new Date();
  await session.save();

  const coach = await User.findById(session.coachId).select('firstName lastName');
  const coachName = coach ? `${coach.firstName} ${coach.lastName}` : 'your coach';
  const intakeUrl = `${config.frontendUrl}/intake/${template._id}?sessionId=${session._id}`;

  await sendEmail({
    to: coachee.email,
    subject: `Post-session reflection — ${sessionDate}`,
    html: buildPostSessionEmailHtml({
      coacheeName: coachee.firstName,
      coachName,
      sessionDate,
      intakeUrl,
    }),
  });

  notifyPostSessionForm({
    coacheeId: session.coacheeId,
    organizationId: session.organizationId as any,
    coachName,
    sessionDate,
    intakeUrl,
  }).catch((err) => console.error('[PostSession] Hub notification failed:', err));

  console.log(`[PostSession] Sent reflection to ${coachee.email} for session ${session._id}`);
}

